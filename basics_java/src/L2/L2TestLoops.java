public class L2TestLoops {

    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World!");
        }

        int counter = 0;
        while (counter++ < 5) {
            System.out.println("Bye World");
        }
    }
}
